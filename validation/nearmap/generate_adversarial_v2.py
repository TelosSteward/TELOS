def main():
    """Generate updated JSONL with adversarial metadata and new scenarios."""
    # Read existing JSONL
    existing = []
    with open(DATASET_PATH) as f:
        for line in f:
            line = line.strip()
            if line:
                existing.append(json.loads(line))

    print(f"Read {len(existing)} existing scenarios")

    # Backfill attack_metadata on existing Cat E scenarios
    backfilled = 0
    for scenario in existing:
        sid = scenario["scenario_id"]
        if sid in EXISTING_CAT_E_METADATA:
            scenario["attack_metadata"] = EXISTING_CAT_E_METADATA[sid]
            backfilled += 1

    print(f"Backfilled attack_metadata on {backfilled} existing Cat E scenarios")

    # Append new adversarial scenarios (idempotent — skip if already present)
    existing_ids = {s["scenario_id"] for s in existing}
    new_added = 0
    for scenario in NEW_ADVERSARIAL:
        if scenario["scenario_id"] not in existing_ids:
            existing.append(scenario)
            existing_ids.add(scenario["scenario_id"])
            new_added += 1
    print(f"Added {new_added} new Cat E adversarial scenarios (skipped {len(NEW_ADVERSARIAL) - new_added} already present)")

    # Append false-positive controls (idempotent — skip if already present)
    ctrl_added = 0
    for scenario in FALSE_POSITIVE_CONTROLS:
        if scenario["scenario_id"] not in existing_ids:
            existing.append(scenario)
            existing_ids.add(scenario["scenario_id"])
            ctrl_added += 1
    print(f"Added {ctrl_added} false-positive controls (skipped {len(FALSE_POSITIVE_CONTROLS) - ctrl_added} already present)")

    # Write updated JSONL
    with open(DATASET_PATH, "w") as f:
        for scenario in existing:
            f.write(json.dumps(scenario, ensure_ascii=False) + "\n")

    print(f"\nTotal scenarios written: {len(existing)}")

    # Summary statistics
    cat_counts = {}
    for s in existing:
        cat = s["boundary_category"]
        cat_counts[cat] = cat_counts.get(cat, 0) + 1
    print("\nPer-category counts:")
    for cat in sorted(cat_counts):
        print(f"  Category {cat}: {cat_counts[cat]}")

    # Attack family distribution for Cat E
    families = {}
    for s in existing:
        if s["boundary_category"] == "E" and "attack_metadata" in s:
            fam = s["attack_metadata"]["attack_family"]
            families[fam] = families.get(fam, 0) + 1
    print("\nCat E attack family distribution:")
    for fam in sorted(families):
        print(f"  {fam}: {families[fam]}")

    # Control pairs
    controls_with_pairs = sum(
        1 for s in existing
        if s.get("attack_metadata", {}).get("control_pair_id")
    )
    print(f"\nScenarios with control_pair_id: {controls_with_pairs}")


if __name__ == "__main__":
    main()
